DATA SEGMENT PUBLIC 'DATA'
	X 	DW 3
	Y 	DW 2
DATA ENDS

My_Stack SEGMENT PARA PUBLIC 'STACK'
		DB 8 DUP(?)
My_Stack ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS: CODE, SS:My_Stack, DS: DATA
START PROC FAR

	PUSH 	DS
	MOV 	AX,0
	PUSH 	AX
	MOV 	AX,DATA
	MOV 	DS,AX
	
	MOV 	AX,X
	PUSH 	AX
	MOV 	AX,Y
	PUSH 	AX
	
	MOV 	BP,SP
	MOV 	BX,[BP]
	MOV 	CX,[BP+2]
	ADD 	SP,4
RET
START ENDP
CODE ENDS
END START