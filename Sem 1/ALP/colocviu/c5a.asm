TITLE SEPARARE NUMERE

STACK1 SEGMENT PARA STACK 'STACK' use16
	DB		64 DUP('my_stack')
STACK1 ENDS

DATA1 SEGMENT PARA PUBLIC 'DATA' use16

DATA1 ENDS

CODE1 SEGMENT PARA PUBLIC 'CODE' use16
MAIN PROC FAR
	ASSUME CS:CODE1, DS:DATA1, SS:STACK1
	PUSH DS
	XOR AX, AX
	PUSH AX
	MOV AX, DATA1
	MOV DS, AX
	
	;DASH=2DH
	;ENTER=0DH
	;SPACE=20H
	
	MOV SI, 0; IF IT REMAINS UNCHANGED, THE NUMBER IS POSITIVE
	MOV CX, 0
	MOV DX, 0
	MOV BX, 0
	
	CITIRE1:
		MOV AH, 01H
		INT 21H
		CMP AL, 0DH
		JE TERMINARE
		CMP AL, 20H
		JE NEXT
		CMP AL, 2Dh
		JE CITIRE2
		JNE CONTINUE
		
	CITIRE2:
		MOV AH, 01H
		INT 21H
		MOV SI, 1

	CONTINUE:
		MOV AL, BL	;SAVE READ VALUE
		MOV AX, CX	;
		MOV SP, 10	;
		MUL SP
		ADD AX, BX	; ADD READ VALUE
		MOV CX, AX	; SAVE NUMBER
		
	NEXT:
		CMP SI, 0H
		JE TERMINARE
		NEG CX
		
	TERMINARE:
		RET
		
		MAIN ENDP
	CODE1 ENDS
END MAIN
		
		