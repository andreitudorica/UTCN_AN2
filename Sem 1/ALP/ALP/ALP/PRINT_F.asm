PUBLIC PrintNumber 
DATA1 SEGMENT PARA 'DATA'
TEN DW 10
DATA1 ENDS


CODE  SEGMENT
        ASSUME  CS : CODE ,DS:DATA1
PUSH DS
 XOR AX,AX
 PUSH AX
MOV AX,DATA1
MOV DS,AX
PrintNumber proc FAR
POP AX
DIVIDE:
	XOR DX,DX
	DIV TEN; RESTUL RAMANE IN DX
	PUSH DX
	INC CX
	CMP AX,0
	JNE DIVIDE
MOV AH,02H
PRINT:
	POP DX
	ADD DL,48
	INT 21H
	DEC CX
	CMP CX,0
	JNE PRINT
ret
PrintNumber endp
CODE ENDS
END