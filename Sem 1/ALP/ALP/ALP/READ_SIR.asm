DATA SEGMENT PARA PUBLIC 'DATA'
SIR DB 100 DUP (0)
SIR2 DB 100 DUP (0)
MESAJ1 DB "PRIMUL SIR ESTE: ","$";
MESAJ2 DB 13,10,"AL DOILEA SIR ESTE: ","$";
COUNT DW ?
COUNT2 DW 0
CONT DW ?
TEN DW 10
OK DB 0
DATA ENDS
CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE, DS:DATA

PrintNumber proc
DIVIDE:
	XOR DX,DX
	DIV TEN; RESTUL RAMANE IN DX
	PUSH DX
	INC CONT
	CMP AX,0
	JNE DIVIDE
MOV AH,02H
PRINT:
	POP DX
	ADD DL,48
	INT 21H
	DEC CONT
	CMP CONT,0
	JNE PRINT
ret
PrintNumber endp

START PROC FAR
 PUSH DS
 XOR AX,AX
 PUSH AX
MOV AX,DATA
MOV DS,AX
XOR SI,SI
XOR CX,CX
XOR BX,BX
MOV AH,01H
BUCLA:
	CMP OK,1
	JE FINAL
	MOV AH , 01H
	INT 21H
	CMP AL, 0DH ; IF ENTER WAS PRESED
	JE NEXT_ALT
	CMP AL, 20H ; IF SPACE WAS PRESSED
	JE NEXT
	MOV CL , AL
	MOV AX , BX
	MUL TEN
	ADD AL, CL
	MOV BX , AX
	SUB BX , 48
	JMP BUCLA
	
NEXT_ALT:
	MOV OK,1
	JMP NEXT

NEXT:
	CMP BL , 120
	JG COMP
	MOV SIR[SI] , BL
	INC SI
	XOR BX,BX
	JMP BUCLA
COMP:
	CMP BL,130
	JS PUT_SECOND
	MOV SIR[SI] , BL
	INC SI
	XOR BX,BX
	JMP BUCLA
PUT_SECOND:
	MOV DX , SI
	MOV SI,COUNT2
	MOV SIR2[SI] , BL
	MOV SI,DX
	INC COUNT2
	XOR BX,BX
	JMP BUCLA
	
	

FINAL:
	;MOV SIR[SI] , BL

MOV AH,09H
LEA DX, MESAJ1
INT 21H

MOV COUNT, SI
XOR CX,CX
XOR SI,SI
NEXT1:
	CMP SI,COUNT
	JE NEXT2
	XOR AX,AX
	MOV AL,SIR[SI]
	INC SI
	MOV CONT,0
	CALL PrintNumber
	MOV AH , 02H
	MOV DL , 20H
	INT 21H
	JMP NEXT1
NEXT2:
	XOR SI,SI
	MOV AH,09H
	LEA DX , MESAJ2
	INT 21H
NEXT3:
	CMP SI,COUNT2
	JE FINAL2
	XOR AX,AX
	MOV AL,SIR2[SI]
	INC SI
	MOV CONT ,0
	CALL PrintNumber
	MOV AH , 02H
	MOV DL , 20H
	INT 21H
	JMP NEXT3
FINAL2:
	
	

;OTHER PROGRAM INSTRUCTIONS
RET
START ENDP
CODE ENDS
END START