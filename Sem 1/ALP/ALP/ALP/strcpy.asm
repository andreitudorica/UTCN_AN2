DATA1 SEGMENT PARA PUBLIC 'DATA'
	STRIN1 DB 'abcdefgHijklmn'
	STRIN1LG DW $-STRIN1
DATA1 ENDS

DATA2 SEGMENT PARA PUBLIC 'DATA'
	STRIN2 DB 10 dup(?)
DATA2 ENDS

COD SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:COD,DS:DATA1,ES:DATA2
START PROC FAR 

	XOR 	AX,AX
	PUSH 	DS
	PUSH 	AX
	MOV 	AX,DATA1
	MOV 	DS,AX


	MOV 	SI,0
	MOV 	CX,STRIN1LG
L:
	MOV 	AX,DATA1
	MOV 	DS,AX
	MOV 	AL,STRIN1[SI]
	MOV 	BX,DATA2 
	MOV 	ES,BX
	XCHG 	AL,BL
	MOV 	STRIN2[SI],BL
	INC 	SI
LOOP L

	Mov 	AH,01H
	INT 	21H
	
	MOV 	STRIN2[SI],'$'

	MOV 	SI,0

   
    LEA 	DX,STRIN2
	Mov 	AH,09H
	INT 	21H

	Mov 	AH,01H
	INT 	21H

RET
ENDP START
COD ENDS
END START