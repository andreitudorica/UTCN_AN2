
CODE1 SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE1

PUBLIC READ

READ PROC FAR
	PUSH BX
	MOV CX,0
P1:	MOV AH,01H
	INT 21H	 
	CMP AL,0DH
	JE P2
	SUB AL,30H
	XOR BL,BL
	MOV BL,AL
	MOV AL,CL
	MOV DL,10
	MUL DL	
	ADD AL,BL
	MOV CL,AL
	JMP P1
P2: POP BX
	MOV BYTE PTR [BX],CL
	
RET
READ ENDP
CODE1 ENDS
END READ