DATA SEGMENT PARA PUBLIC 'DATA'	
	NUM1	DB	5
	NUM2	DB	1
	RES		DB	?
DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
	ASSUME CS:CODE, DS:DATA
START PROC FAR
	PUSH 	DS
	XOR 	AX,AX
	PUSH 	AX
	MOV 	AX,DATA
	MOV 	DS,AX
	XOR		BX,BX
	XOR		CX,CX
	
	MOV 	AL,2
	MOV 	BL,2
	MOV 	Cl,NUM1
	ADD 	Cl,NUM2
	MUL 	Cl
	ADD 	Al,BL
	MOV 	RES,Cl
	
	RET
START ENDP
CODE ENDS
END START